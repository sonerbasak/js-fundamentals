//math methods

//floor: returns the largest integer less than or equal to a given number.
console.log("floor: ", Math.floor(4.7)); // 4

//ceil: returns the smallest integer greater than or equal to a given number.
console.log("ceil: ", Math.ceil(4.3)); // 5

//round: returns the value of a number rounded to the nearest integer.
console.log("round: ", Math.round(4.5)); // 5

//max: returns the largest of zero or more numbers.
console.log("max: ", Math.max(1, 3, 2, 5, 4)); // 5

//min: returns the smallest of zero or more numbers.
console.log("min: ", Math.min(1, 3, 2, 5, 4)); // 1

//random: returns a pseudo-random number between 0 (inclusive) and 1 (exclusive).
console.log("random: ", Math.random()); // e.g., 0.123456789

//pow: returns the base to the exponent power, that is, base^exponent.
console.log("pow: ", Math.pow(2, 3)); // 8

//sqrt: returns the square root of a number.
console.log("sqrt: ", Math.sqrt(16)); // 4

//abs: returns the absolute value of a number.
console.log("abs: ", Math.abs(-7)); // 7

//log: returns the natural logarithm (base e) of a number.
console.log("log: ", Math.log(10)); // ~2.302585092994046

//sin: returns the sine of a number (in radians).
console.log("sin: ", Math.sin(Math.PI / 2)); // 1

//cos: returns the cosine of a number (in radians).
console.log("cos: ", Math.cos(0)); // 1

//tan: returns the tangent of a number (in radians).
console.log("tan: ", Math.tan(Math.PI / 4)); // 1

//exp: returns e (the base of natural logarithms) raised to the power of a given number.
console.log("exp: ", Math.exp(1)); // ~2.718281828459045
