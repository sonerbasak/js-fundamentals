// assignment operators


let a = 10; //  '=' is assignment operator
let b = 5;
let c = "10";

// comparison operator, it doesnt check datatype
console.log("comparison:", a == c);

// strict comparison operator, it checks datatype too
console.log("strict comparison:", a === c);
a += b;
console.log("addition:", a);

a -= b;
console.log("substraction:", a);

a *= b;
console.log("multiplication:", a);

a /= b;
console.log("division:", a);

a %= b;
console.log("modulus:", a);

a **= 2;
console.log("exponentiation:", a);

