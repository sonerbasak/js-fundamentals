// logical operators: && (AND), || (OR), ! (NOT)

const a = true;
const b = true;
const c = false;

console.log("a:", a);
console.log("b:", b);
console.log("c:", c);

// AND operator
console.log("a && b:", a && b);
console.log("a && c:", a && c);

// OR operator
console.log("a || c:", a || c);
console.log("c || c:", c || c);

// NOT operator
console.log("!a:", !a);
console.log("!c:", !c);

